def price_to_str(price):
    ''' Convert the price (float) into its string representation (with comma) '''
    str_price = f'{price:.2f}'
    return str_price.replace('.', ',')
# end def price_to_str

def str_to_price(strprice):
    ''' Convert string into its float value '''
    strprice = strprice.replace(',', '.')
    return float(strprice)
# end def str_to_price


